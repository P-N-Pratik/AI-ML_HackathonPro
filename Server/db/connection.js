const promise=mongoose.connect("mongodb://0.0.0.0:27017/ImpactProject");


















































































































































































































































































































































































































promise.then((res)=>{
    console.log("Connected Successfully");
    console.log(res);
})

promise.catch((error)=>{
    console.log("Error",error);
})



